      SUBROUTINE READ_SVP(LFNAM,NUM,SVP_DAT)
      IMPLICIT REAL*8(A-H,O-Z)

	CHARACTER*180 BUFF180
      DIMENSION  SVP_DAT(3,50000)
      INTEGER(4) NUM,LFNAM

       NUM = 0
       TIME0 = 0.0D0
       READ (LFNAM,*)
         
       DO I = 1,999999
       READ (LFNAM,'(A180)',END=999) BUFF180
        NUM = NUM + 1
        READ (BUFF180,110,END=999) IYEAR,IMONTH,IDAY
     1         ,IHOUR,IMIN,SEC,SVP,DH
110     FORMAT(I4,4(1X,I2),1X,F9.6,2F12.3)

        TEMP = DBLE(IDAY-14)*86400.0D0 + DBLE(IHOUR)*3600.0D0 + 
     1         DBLE(IMIN)*60.0D0 + SEC - TIME0
        SVP_DAT(1,NUM) = TEMP
        SVP_DAT(2,NUM) = DH
        SVP_DAT(3,NUM) = SVP

       ENDDO



999   CONTINUE
       RETURN
	END
